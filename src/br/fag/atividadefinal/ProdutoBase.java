package br.fag.atividadefinal;

abstract class ProdutoBase implements Vendavel {
    private int codigo;
    private String nome;
    private double preco;

    public ProdutoBase(int codigo, String nome, double preco) {
        this.codigo = codigo;
        this.nome = nome;
        this.preco = preco;
    }

    public int getCodigo() {
        return codigo;
    }

    public String getNome() {
        return nome;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
}
